#The ONLY EntryPoint. This is great :)
@api_view(['GET'])
def api_root(request, format=None):
    return Response({
        'users': reverse('user-list', request=request, format=format),
        'snippets': reverse('snippet-list', request=request, format=format)
    })


# To take advantage of the fact that our responses are no longer hardwired to a single content type
#  let's add support for format suffixes to our API endpoints. Using format suffixes gives us URLs
#  that explicitly refer to a given format, and means our API will be able to handle URLs
#  such as http://example.com/api/items/4/.json.

# Notice that we've also used the @detail_route decorator to create a custom action, named highlight.
# This decorator can be used to add any custom endpoints that don't fit into the standard create/update/delete style.

# Custom actions which use the @detail_route decorator will respond to GET requests.
# We can use the methods argument if we wanted an action that responded to POST requests.

#The URLs for custom actions by default depend on the method name itself.
# If you want to change the way url should be constructed, you can include url_path as a decorator keyword argument.

class SnippetViewSet(viewsets.ModelViewSet):
    """
    This viewset automatically provides `list`, `create`, `retrieve`,
    `update` and `destroy` actions.

    Additionally we also provide an extra `highlight` action.
    """
    queryset = Snippet.objects.all()
    serializer_class = SnippetSerializer
    permission_classes = (permissions.IsAuthenticatedOrReadOnly,
                          IsOwnerOrReadOnly,)

    @detail_route(renderer_classes=[renderers.StaticHTMLRenderer])
    def highlight(self, request, *args, **kwargs):
        snippet = self.get_object()
        return Response(snippet.highlighted)

    def perform_create(self, serializer):
        serializer.save(owner=self.request.user)


class SnippetHighlight(generics.GenericAPIView):
    queryset = Snippet.objects.all()
    renderer_classes = (renderers.StaticHTMLRenderer,)

    def get(self, request, *args, **kwargs):
        snippet = self.get_object()
        return Response(snippet.highlighted)

